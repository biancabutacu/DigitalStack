package CreareFisaAngajat;

public class Angajat {
	private String nume;
	private String prenume;
	private int salariuNet;
	private int numarOreDeLucru;
	
	
	public String getNume() {
		return nume;
	}
	public void setNume(String nume) {
		this.nume = nume;
	}
	public String getPrenume() {
		return prenume;
	}
	public void setPrenume(String prenume) {
		this.prenume = prenume;
	}
	public int getSalariuNet() {
		return salariuNet;
	}
	public void setSalariuNet(int salariuNet) {
		this.salariuNet = salariuNet;
	}
	public int getNumarOreDeLucru() {
		return numarOreDeLucru;
	}
	public void setNumarOreDeLucru(int numarOreDeLucru) {
		this.numarOreDeLucru = numarOreDeLucru;
	}
	
	
}

